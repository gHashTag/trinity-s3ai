(* Section 4: T1 master theorem — Cl(p,q) spec is internally consistent.      *)
(*                                                                            *)
(* "Internally consistent" here means: the spec record `CliffordSpec p q` is *)
(* not contradictory; any concrete model (e.g. matrices in low dim) gives a  *)
(* witness. We do NOT construct a witness for arbitrary (p,q) — that is what *)
(* mathlib4's CliffordAlgebra provides, and porting that is multi-month work *)
(* (out of scope for T1).                                                    *)
(*                                                                            *)
(* What we PROVE here is the statement-level coherence: from the spec one    *)
(* can derive the standard immediate facts (Clifford relation, linearity of  *)
(* the inclusion, universal factorization). Witness construction is left to  *)
(* Cl6_iso_M8R.v (T2) and Cl8_periodicity.v (T3), where for the specific     *)
(* signatures we either build a model explicitly or admit with citation.     *)
(******************************************************************************)

Theorem T1_clifford_spec_coherent :
  forall p q (C : CliffordSpec p q) (v : Vec (p + q)),
    alg_mul (cl_alg C) (cl_inc C v) (cl_inc C v) =
    alg_smul (cl_alg C) (Q_pq p q v) (alg_one (cl_alg C)).
Proof.
  intros p q C v.
  apply (cl_sq C v).
Qed.

(* The inclusion respects the R-vector-space structure (a free corollary of
   the spec, but we state it as a named theorem for downstream use). *)
Theorem T1_inc_linear :
  forall p q (C : CliffordSpec p q) (a : R) (u v : Vec (p + q)),
    cl_inc C (vec_add (vec_smul a u) v) =
    alg_add (cl_alg C)
            (alg_smul (cl_alg C) a (cl_inc C u))
            (cl_inc C v).
Proof.
  intros p q C a u v.
  rewrite (cl_inc_add _ _ C).
  rewrite (cl_inc_smul _ _ C).
  reflexivity.
Qed.

(* The polarization identity: i(u)·i(v) + i(v)·i(u) = (Q(u+v) − Q(u) − Q(v))·1.
   This is the standard rewriting of the Clifford relation in terms of the
   symmetric bilinear form B(u,v) = ½(Q(u+v) − Q(u) − Q(v)).
   Proof: expand (i(u)+i(v))² via distributivity, apply cl_sq to each
   squared term, then cancel the Q(u)·1 and Q(v)·1 terms using the
   additive-inverse axioms of RAlgebra. *)
Theorem T1_polarization :
  forall p q (C : CliffordSpec p q) (u v : Vec (p + q)),
    alg_add (cl_alg C)
            (alg_mul  (cl_alg C) (cl_inc C u) (cl_inc C v))
            (alg_mul  (cl_alg C) (cl_inc C v) (cl_inc C u))
    =
    alg_add (cl_alg C)
            (alg_smul (cl_alg C) (Q_pq p q (vec_add u v))
                                 (alg_one (cl_alg C)))
            (alg_add (cl_alg C)
              (alg_smul (cl_alg C) (- Q_pq p q u)
                                 (alg_one (cl_alg C)))
              (alg_smul (cl_alg C) (- Q_pq p q v)
                                 (alg_one (cl_alg C)))).
Proof.
  intros p q C u v.
  set (A := cl_alg C).
  set (a := cl_inc C u).
  set (b := cl_inc C v).
  set (one := alg_one A).

  (* Step 1: Clifford relation on u+v, u, and v. *)
  assert (Huv : alg_mul A (alg_add A a b) (alg_add A a b) =
                alg_smul A (Q_pq p q (vec_add u v)) one).
  { unfold A, a, b, one. rewrite <- (cl_inc_add p q C). apply cl_sq. }

  assert (Hu : alg_mul A a a = alg_smul A (Q_pq p q u) one).
  { apply cl_sq. }

  assert (Hv : alg_mul A b b = alg_smul A (Q_pq p q v) one).
  { apply cl_sq. }

  (* Step 2: Expand (a+b)*(a+b) using distributivity. *)
  assert (Hexp : alg_mul A (alg_add A a b) (alg_add A a b) =
                 alg_add A (alg_add A (alg_mul A a a) (alg_mul A a b))
                            (alg_add A (alg_mul A b a) (alg_mul A b b))).
  {
    rewrite (alg_distr_r A a b (alg_add A a b)).
    rewrite (alg_distr_l A a a b).
    rewrite (alg_distr_l A b a b).
    reflexivity.
  }

  (* Step 3: Substitute cl_sq results into the expansion. *)
  rewrite Hexp in Huv.
  rewrite Hu in Huv.
  rewrite Hv in Huv.

  (* Step 4: Rearrange using associativity/commutativity so that
     the scalar terms are collected on the left. *)
  assert (Hrearr :
    alg_add A (alg_add A (alg_smul A (Q_pq p q u) one)
                              (alg_smul A (Q_pq p q v) one))
              (alg_add A (alg_mul A a b) (alg_mul A b a))
    = alg_smul A (Q_pq p q (vec_add u v)) one).
  { rewrite <- (alg_add_shuffle A).
    rewrite (alg_add_comm A (alg_smul A (Q_pq p q v) one) (alg_mul A b a)).
    exact Huv. }

  (* Step 5: Cancel the scalar terms using additive inverses. *)
  apply (alg_add_cancel A (alg_add A (alg_smul A (Q_pq p q u) one)
                                        (alg_smul A (Q_pq p q v) one)))
    in Hrearr.

  (* Step 6: Distribute the inverse over the sum. *)
  rewrite (alg_opp_add A (alg_smul A (Q_pq p q u) one)
                          (alg_smul A (Q_pq p q v) one)) in Hrearr.

  (* Step 7: Rewrite negative scalars. *)
  rewrite <- (alg_smul_opp A (Q_pq p q u) one) in Hrearr.
  rewrite <- (alg_smul_opp A (Q_pq p q v) one) in Hrearr.

  exact Hrearr.
Qed.

(* Sanity: the spec also guarantees that two algebra maps from cl_alg into
   any target that agree on the image of cl_inc must be equal everywhere.
   This is the uniqueness part of the universal property. We re-export it
   as a named theorem. *)
Theorem T1_universal_uniqueness :
  forall p q (C : CliffordSpec p q) (B : RAlgebra)
         (j : Vec (p + q) -> carrier B)
         (f1 f2 : AlgHom (cl_alg C) B),
    (forall v, hom_fn f1 (cl_inc C v) = j v) ->
    (forall v, hom_fn f2 (cl_inc C v) = j v) ->
    forall x, hom_fn f1 x = hom_fn f2 x.
Proof.
  intros p q C B j f1 f2 H1 H2 x.
  apply (cl_univ_unique _ _ C B j f1 f2 H1 H2).
Qed.

