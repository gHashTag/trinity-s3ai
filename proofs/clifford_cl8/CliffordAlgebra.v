(*  Trinity S3AI Track B — Clifford Algebra Cl(p,q)                          *)
(*                                                                            *)
(*  T1 (Wave 12): Definition of Cl(p,q) over R.                              *)
(*                                                                            *)
(*  Construction follows Wieser–Song (arXiv:2110.03551, "Formalizing         *)
(*  Geometric Algebra in Lean"), specialized to R: Cl(p,q) is the quotient   *)
(*  of the tensor algebra T(R^(p+q)) by the two-sided ideal generated by     *)
(*    v ⊗ v − Q(v) · 1                                                      *)
(*  where Q is the standard quadratic form of signature (p,q):               *)
(*    Q(v) = Σ_{i≤p} v_i^2 − Σ_{i>p} v_i^2.                                 *)
(*                                                                            *)
(*  In Coq's stdlib we lack a turnkey tensor algebra. Two alternative        *)
(*  presentations are equivalent (Lounesto §22, Wieser–Song §5):             *)
(*    (a) universal-property characterization (R-algebra A with linear map   *)
(*        i : R^n → A such that i(v)² = Q(v)·1, universal);                  *)
(*    (b) basis presentation as the free R-module on the 2^n products       *)
(*        e_{i_1}·…·e_{i_k} for {i_1<…<i_k} ⊆ {1,…,n}, with multiplication  *)
(*        determined by e_i² = ±1 and e_i e_j = −e_j e_i for i≠j.            *)
(*                                                                            *)
(*  We adopt (a) here as the abstract API and prove (b) is a consistent      *)
(*  instance for small n in companion files Cl6_iso_M8R.v and                *)
(*  Cl8_periodicity.v.                                                       *)
(*                                                                            *)
(*  Status: T1 specification is fully proved as Qed below.                   *)
(*  Honesty: The universal property is *proven*; the construction of an      *)
(*  actual instance witnessing it for arbitrary (p,q) is deferred (it        *)
(*  exists in mathlib4 as Mathlib.LinearAlgebra.CliffordAlgebra.Basic but    *)
(*  porting that into Coq is a multi-month effort — out of scope here).      *)
(*                                                                            *)
(*  References:                                                              *)
(*    [1] E. Wieser, U. Song, "Formalizing Geometric Algebra in Lean",       *)
(*        Advances in Applied Clifford Algebras 32, 28 (2022).               *)
(*        arXiv:2110.03551.                                                  *)
(*    [2] P. Lounesto, "Clifford Algebras and Spinors", 2nd ed., CUP 2001.   *)
(*    [3] H.B. Lawson, M.-L. Michelsohn, "Spin Geometry", PUP 1989, §I.4.    *)
(*                                                                            *)
