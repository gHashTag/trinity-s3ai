(* Section 1: Quadratic form of signature (p, q) on R^n with n = p + q.       *)
(*                                                                            *)
(* We model vectors as functions Fin n -> R rather than introducing Vector    *)
(* dependencies, to keep the file self-contained.                             *)
(******************************************************************************)

(* Index type {0, ..., n-1} via bounded nats. *)
Record Fin (n : nat) := mkFin {
  fin_val : nat;
  fin_lt  : (fin_val < n)%nat
}.

Arguments mkFin {n} _ _.
Arguments fin_val {n} _.
Arguments fin_lt  {n} _.

(* A vector in R^n is a function on its index set. *)
Definition Vec (n : nat) : Type := Fin n -> R.

(* Pointwise zero, addition, and scalar multiplication on Vec n. *)
Definition vec_zero (n : nat) : Vec n := fun _ => 0.
Definition vec_add  {n} (u v : Vec n) : Vec n := fun i => u i + v i.
Definition vec_smul {n} (a : R) (v : Vec n) : Vec n := fun i => a * v i.

(* The standard signature-(p,q) quadratic form on R^{p+q}.
   To avoid dependent-pattern-match gymnastics for Fin n, we work with a
   helper that takes a "padded" sequence (nat -> R) returning 0 outside its
   intended range. The natural quadratic form is then summed over k < n. *)

Fixpoint sum_sq_signed (n : nat) (p : nat) (v : nat -> R) : R :=
  match n with
  | O => 0
  | S k =>
      let term := if Nat.ltb k p then v k * v k else - (v k * v k) in
      term + sum_sq_signed k p v
  end.

(* Lift a Vec (p+q) to a padded (nat -> R). Outside its range it returns 0. *)
Definition vec_pad {n} (v : Vec n) : nat -> R :=
  fun k =>
    match lt_dec k n with
    | left H  => v (mkFin k H)
    | right _ => 0
    end.

Definition Q_pq (p q : nat) (v : Vec (p + q)) : R :=
  sum_sq_signed (p + q) p (vec_pad v).

(* Sanity check: the signed sum-of-squares on the constant-zero function is 0. *)
Lemma sum_sq_signed_zero :
  forall n p, sum_sq_signed n p (fun _ => 0) = 0.
Proof.
  induction n as [|k IH]; intros p; simpl.
  - reflexivity.
  - rewrite IH. destruct (Nat.ltb k p); lra.
Qed.

(* The padded form of the zero vector is the constant-zero function. *)
Lemma vec_pad_vec_zero :
  forall n k, vec_pad (vec_zero n) k = 0.
Proof.
  intros n k. unfold vec_pad, vec_zero.
  destruct (lt_dec k n); reflexivity.
Qed.

(* Two padded sequences that agree pointwise give the same signed sum. *)
Lemma sum_sq_signed_ext :
  forall n p f g,
    (forall k, f k = g k) ->
    sum_sq_signed n p f = sum_sq_signed n p g.
Proof.
  induction n as [|k IH]; intros p f g Hfg; simpl.
  - reflexivity.
  - rewrite (IH p f g Hfg). rewrite Hfg. reflexivity.
Qed.

Lemma Q_pq_zero : forall p q, Q_pq p q (vec_zero (p + q)) = 0.
Proof.
  intros p q. unfold Q_pq.
  transitivity (sum_sq_signed (p + q) p (fun _ : nat => 0)).
  - apply sum_sq_signed_ext. apply vec_pad_vec_zero.
  - apply sum_sq_signed_zero.
Qed.

