(* Section 3: Basic consequences of the universal property.                   *)
(*                                                                            *)
(* These follow directly from the Clifford defining relation cl_sq and the   *)
(* R-algebra axioms — no further commitment to a particular construction is  *)
(* needed.                                                                    *)
(******************************************************************************)

Section CliffordCorollaries.

Variables p q : nat.
Variable C : CliffordSpec p q.

(* The image of the zero vector lies at zero in the algebra. *)
Lemma cl_inc_zero_carrier :
  cl_inc C (vec_zero (p + q)) = alg_zero (cl_alg C).
Proof. apply (cl_inc_zero _ _ C). Qed.

(* The Clifford relation, restated as a Lemma for downstream use. *)
Lemma cl_sq_lemma :
  forall v,
    alg_mul (cl_alg C) (cl_inc C v) (cl_inc C v) =
    alg_smul (cl_alg C) (Q_pq p q v) (alg_one (cl_alg C)).
Proof. intros v. apply (cl_sq C v). Qed.

End CliffordCorollaries.

