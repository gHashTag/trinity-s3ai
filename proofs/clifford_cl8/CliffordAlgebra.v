(* Section 2: Abstract Clifford-algebra spec via the universal property.      *)
(*                                                                            *)
(* A Clifford algebra of (V, Q) is an R-algebra A together with an R-linear  *)
(* map i : V → A such that i(v)² = Q(v) · 1, and which is universal for       *)
(* this property among R-algebras receiving V with the same condition.       *)
(*                                                                            *)
(* We package this as a record. The "universal" clause is the existence and  *)
(* uniqueness of an R-algebra map A → B factoring any valid (B, j).          *)
(******************************************************************************)

(* Lightweight R-algebra structure (associative unital R-algebra).
   We include additive inverses and scalar-distributivity over scalar
   addition so that polynomial identities (polarization, etc.) can be
   derived directly from the axioms. *)
Record RAlgebra : Type := {
  carrier      :> Type;
  alg_zero     : carrier;
  alg_one      : carrier;
  alg_add      : carrier -> carrier -> carrier;
  alg_mul      : carrier -> carrier -> carrier;
  alg_smul     : R -> carrier -> carrier;
  alg_opp      : carrier -> carrier;
  (* Axioms — kept minimal; the structural identities we actually use below
     in proofs are recovered from these. *)
  alg_add_assoc :
    forall a b c, alg_add (alg_add a b) c = alg_add a (alg_add b c);
  alg_add_comm  :
    forall a b, alg_add a b = alg_add b a;
  alg_add_0_l   :
    forall a, alg_add alg_zero a = a;
  alg_add_opp_l :
    forall a, alg_add (alg_opp a) a = alg_zero;
  alg_mul_assoc :
    forall a b c, alg_mul (alg_mul a b) c = alg_mul a (alg_mul b c);
  alg_mul_1_l   :
    forall a, alg_mul alg_one a = a;
  alg_mul_1_r   :
    forall a, alg_mul a alg_one = a;
  alg_distr_l   :
    forall a b c, alg_mul a (alg_add b c) =
                  alg_add (alg_mul a b) (alg_mul a c);
  alg_distr_r   :
    forall a b c, alg_mul (alg_add a b) c =
                  alg_add (alg_mul a c) (alg_mul b c);
  alg_smul_1    :
    forall a, alg_smul 1 a = a;
  alg_smul_mul  :
    forall r s a, alg_smul (r * s) a = alg_smul r (alg_smul s a);
  alg_smul_add_distr :
    forall r s a, alg_smul (r + s) a = alg_add (alg_smul r a) (alg_smul s a);
  alg_smul_add_l :
    forall r a b, alg_smul r (alg_add a b) = alg_add (alg_smul r a) (alg_smul r b);
  (* Compatibility of scalar multiplication with algebra multiplication *)
  alg_smul_mul_l :
    forall r x y, alg_mul (alg_smul r x) y = alg_smul r (alg_mul x y);
  alg_smul_mul_r :
    forall r x y, alg_mul x (alg_smul r y) = alg_smul r (alg_mul x y)
}.

(* The R-algebra homomorphism between two RAlgebras. *)
Record AlgHom (A B : RAlgebra) : Type := {
  hom_fn     : carrier A -> carrier B;
  hom_zero   : hom_fn (alg_zero A) = alg_zero B;
  hom_one    : hom_fn (alg_one  A) = alg_one  B;
  hom_add    : forall x y,
                 hom_fn (alg_add A x y) = alg_add B (hom_fn x) (hom_fn y);
  hom_mul    : forall x y,
                 hom_fn (alg_mul A x y) = alg_mul B (hom_fn x) (hom_fn y);
  hom_smul   : forall r x,
                 hom_fn (alg_smul A r x) = alg_smul B r (hom_fn x)
}.

Arguments hom_fn   {A B} _.
Arguments hom_zero {A B} _.
Arguments hom_one  {A B} _.
Arguments hom_add  {A B} _.
Arguments hom_mul  {A B} _.
Arguments hom_smul {A B} _.

(* The universal property of the Clifford algebra over a signature-(p,q)
   quadratic form. *)
Record CliffordSpec (p q : nat) : Type := {
  (* The underlying R-algebra. *)
  cl_alg : RAlgebra;
  (* The R-linear inclusion of the vector space. *)
  cl_inc : Vec (p + q) -> carrier cl_alg;
  cl_inc_zero :
    cl_inc (vec_zero (p + q)) = alg_zero cl_alg;
  cl_inc_add :
    forall u v,
      cl_inc (vec_add u v) =
      alg_add cl_alg (cl_inc u) (cl_inc v);
  cl_inc_smul :
    forall a v,
      cl_inc (vec_smul a v) =
      alg_smul cl_alg a (cl_inc v);
  (* The Clifford defining relation: i(v)^2 = Q(v) · 1. *)
  cl_sq :
    forall v,
      alg_mul cl_alg (cl_inc v) (cl_inc v) =
      alg_smul cl_alg (Q_pq p q v) (alg_one cl_alg);
  (* Universal property: any other R-algebra map satisfying the Clifford
     relation factors uniquely through cl_alg. *)
  cl_univ :
    forall (B : RAlgebra) (j : Vec (p + q) -> carrier B),
      (forall v w, j (vec_add v w) = alg_add B (j v) (j w)) ->
      (forall r v, j (vec_smul r v) = alg_smul B r (j v)) ->
      (forall v, alg_mul B (j v) (j v) =
                 alg_smul B (Q_pq p q v) (alg_one B)) ->
      (* Existence of a factoring algebra map. *)
      { f : AlgHom cl_alg B
            | forall v, hom_fn f (cl_inc v) = j v };
  cl_univ_unique :
    forall (B : RAlgebra) (j : Vec (p + q) -> carrier B)
           (f1 f2 : AlgHom cl_alg B),
      (forall v, hom_fn f1 (cl_inc v) = j v) ->
      (forall v, hom_fn f2 (cl_inc v) = j v) ->
      forall x, hom_fn f1 x = hom_fn f2 x
}.

Arguments cl_alg  {p q} _.
Arguments cl_inc  {p q} _ _.
Arguments cl_sq   {p q} _ _.

