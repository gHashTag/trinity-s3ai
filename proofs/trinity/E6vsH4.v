(* Theorem E6_no_phi: Golden ratio phi does NOT appear in any E6 invariant.   *)
(*                                                                              *)
(* Proof: All E6 invariants are integers or rational numbers.                  *)
(*        phi is irrational, therefore cannot be derived from E6 invariants    *)
