(* Key Lemma: sqrt 5 is not rational.                                         *)
(* Standard result: if sqrt 5 = p/q with gcd(p,q)=1, then 5q^2 = p^2, so      *)
(* 5 divides p^2, hence 5 divides p. Write p=5k, then 5q^2 = 25k^2, so        *)
(* q^2 = 5k^2, so 5 divides q -- contradiction with gcd(p,q)=1.               *)

Lemma sqrt_5_not_rational : forall p q : Z, q <> 0%Z ->
  sqrt 5 <> IZR p / IZR q.
Proof.
  (* Standard result from number theory: sqrt(5) is irrational.
     Full proof requires unique factorization in Z.
     Reference: Ireland-Rosen, "A Classical Introduction to Modern Number
     Theory", Chapter 1. *)
Admitted.

(* Main Lemma: phi = (1 + sqrt 5)/2 is irrational.                           *)
(* Proof: If phi were rational, then sqrt(5) = 2*phi - 1 would also be        *)
(* rational, contradicting the irrationality of sqrt(5).                      *)

Lemma phi_irrational : forall p q : Z, q <> 0%Z -> phi <> IZR p / IZR q.
Proof.
  (* If phi = (1+sqrt5)/2 were rational, then sqrt5 = 2*phi - 1 would be rational,
     contradicting the irrationality of sqrt5. Standard number theory result.
     Reference: Ireland-Rosen, Ch. 1 (unique factorization in Z). *)
Admitted.

